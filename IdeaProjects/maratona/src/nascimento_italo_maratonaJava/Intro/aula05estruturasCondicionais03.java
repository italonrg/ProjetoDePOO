package nascimento_italo_maratonaJava.Intro;

public class aula05estruturasCondicionais03 {
    public static void main(String[] args) {

        ///
/// operador ternario sintaxe (condição ) verdadeiro : falso;
        double salario = 6000;
        String mensagemDoar = "ghghhg";
        String mensagemNãoDoar = "ghghhg";
        String resulatado = salario>5000 ? mensagemDoar: mensagemNãoDoar;

        System.out.println(resulatado);
/// operadores ternarios devem ser utilizados me if e elses simples
        /// eles podem ser aninhados mas não e recomendado

    }
}
